//Calculate my average shopping bill

//Storing my 5 grocery totals in an array
var week = [40, 60, 30, 20, 55];

//Calculating total costs
var total = week[0] + week[1] + week[2] + week[3] + week[4];

//Creating an expression to calculate the average
var average = total / 5;

//Printing out the total and average of my shopping bills
console.log("You have spent a total of $" + total + " on groceries over 5 weeks. This is an average of $" + average + " per week.");


 